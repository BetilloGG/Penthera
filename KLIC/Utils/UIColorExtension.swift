import Foundation
import UIKit

extension UIColor {

    class var yellowColors: UIColor {
        return UIColor(red: 253 / 255, green: 197 / 255, blue: 4 / 255, alpha: 1.0)
    }

    class var yellowColorsRefresh: UIColor {
        return UIColor(red: 220 / 255, green: 197 / 255, blue: 4 / 255, alpha: 1.0)
    }

    @nonobjc class var kiSteelGrey: UIColor {
        return UIColor(red: 129.0 / 255.0, green: 129.0 / 255.0, blue: 130.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var foxGrey: UIColor {
        return UIColor(red: 0.203015, green: 0.20302, blue: 0.203017, alpha: 1.0)
    }

    @nonobjc class var foxGreyNormal: UIColor {
        return UIColor(red: 0.0941176, green: 0.0941176, blue: 0.0941176, alpha: 1.0)
       }

    @nonobjc class var foxGreyDisable: UIColor {
        return UIColor(red: 24.0 / 255.0, green: 24.0 / 255.0, blue: 24.0 / 255.0, alpha: 1.0)
    }
}
